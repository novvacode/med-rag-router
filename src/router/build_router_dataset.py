"""
src/router/build_router_dataset.py

Router Training Dataset Generator.

Processes the non-held-out router QA splits and executes retrieval in all 
3 modes (T, T+E, T+E+K). Generates a dataset where each question has 3 rows 
representing the context retrieved by each mode. 

FIX (2026-07-10):
    ehrqa_router_train.parquet and ehrqa_router_val.parquet are generated by
    generate_synthetic_ehrqa_split.py, which only produces QA pairs and does
    NOT include sparsity columns (n_labs, n_diag, sparsity_score,
    sparsity_bucket). build_router_dataset.py was reading those columns
    directly from the QA file, getting NaN/missing, and silently falling back
    to sparsity_bucket="unknown" for every row.

    Fix: after loading df_qa, join data/lakehouse/sparsity.parquet on
    hadm_id. This populates n_labs, n_diag, sparsity_score, and
    sparsity_bucket with real values before per-row extraction.

    No other logic is changed.

Output Schema:
    - question
    - answer
    - hadm_id
    - subject_id
    - mode
    - retrieved_chunks (JSON serialized)
    - has_ehr
    - n_kg_facts
    - prompt_context
    - prompt_token_estimate
    - retrieval_latency_ms
    - n_labs
    - n_diag
    - n_meds
    - sparsity_score
    - sparsity_bucket

Usage:
    python -m src.router.build_router_dataset
"""

from __future__ import annotations

import json
import time
from pathlib import Path
from typing import Dict, Any, Tuple

import numpy as np
import pandas as pd

from src.retrieval.retriever import Retriever, Mode

# ── Config ────────────────────────────────────────────────────────────────────

INPUT_TRAIN_A = Path("data/lakehouse/qa/ehrqa_router_train.parquet")
INPUT_TRAIN_B = Path("data/qa/ehrqa_router_train.parquet")

INPUT_VAL_A   = Path("data/lakehouse/qa/ehrqa_router_val.parquet")
INPUT_VAL_B   = Path("data/qa/ehrqa_router_val.parquet")

# FIX: sparsity file path
SPARSITY_FILE = Path("data/lakehouse/sparsity.parquet")

OUT_DIR   = Path("data/router")
OUT_TRAIN = OUT_DIR / "router_train_examples.parquet"
OUT_VAL   = OUT_DIR / "router_val_examples.parquet"


# ── Helpers ───────────────────────────────────────────────────────────────────

def get_input_path(path_a: Path, path_b: Path) -> Path:
    if path_a.exists():
        return path_a
    if path_b.exists():
        return path_b
    raise FileNotFoundError(
        f"Dataset not found at {path_a} or {path_b}. "
        "Please ensure Phase 1 generation is complete."
    )


def load_kg_module():
    try:
        import src.mkg.retrieval as kg_module
        print("[INFO] Neo4j KG module loaded successfully.")
        return kg_module
    except Exception as e:
        print(f"[WARN] Could not initialize KG module: {e}")
        print("[WARN] Proceeding with KG component disabled.")
        return None


def numpy_json_encoder(obj):
    if isinstance(obj, np.generic):
        return obj.item()
    if pd.isna(obj):
        return None
    return str(obj)


# ── FIX: sparsity join ────────────────────────────────────────────────────────

def join_sparsity(df_qa: pd.DataFrame) -> pd.DataFrame:
    """
    Join sparsity.parquet onto the QA DataFrame using hadm_id.

    ehrqa_*.parquet files do not contain sparsity columns — those live
    only in data/lakehouse/sparsity.parquet, which is written by
    src/lakehouse/sparsity.py. This join populates:
        n_labs, n_diag, d_note, sparsity_score, sparsity_bucket

    Rows whose hadm_id is not found in sparsity.parquet receive:
        n_labs=0, n_diag=0, sparsity_score=0.0, sparsity_bucket="unknown"
    (same as the old silent fallback, but now only for truly missing IDs).
    """
    if not SPARSITY_FILE.exists():
        print(f"[WARN] Sparsity file not found: {SPARSITY_FILE}. "
              "All rows will have sparsity_bucket='unknown'. "
              "Run src/lakehouse/sparsity.py first.")
        return df_qa

    sp = pd.read_parquet(SPARSITY_FILE, columns=[
        "hadm_id", "n_labs", "n_diag", "sparsity_score", "sparsity_bucket"
    ])

    # Drop any existing (stale/empty) sparsity columns from the QA file
    stale = [c for c in ["n_labs", "n_diag", "sparsity_score", "sparsity_bucket"]
             if c in df_qa.columns]
    if stale:
        df_qa = df_qa.drop(columns=stale)

    before = len(df_qa)
    df_qa = df_qa.merge(sp, on="hadm_id", how="left")

    matched = df_qa["sparsity_bucket"].notna().sum()
    print(f"[INFO] Sparsity join: {matched}/{before} rows matched "
          f"({100*matched/before:.1f}%)")
    print(f"[INFO] Sparsity bucket distribution after join:")
    print(df_qa["sparsity_bucket"].fillna("unknown").value_counts().to_string())

    # Fill unmatched rows with safe defaults
    df_qa["n_labs"]          = df_qa["n_labs"].fillna(0).astype(int)
    df_qa["n_diag"]          = df_qa["n_diag"].fillna(0).astype(int)
    df_qa["sparsity_score"]  = df_qa["sparsity_score"].fillna(0.0).astype(float)
    df_qa["sparsity_bucket"] = df_qa["sparsity_bucket"].fillna("unknown")

    return df_qa


# ── Core Processing ───────────────────────────────────────────────────────────

def process_split(
    split_name:  str,
    input_path:  Path,
    output_path: Path,
    retriever:   Retriever,
) -> Tuple[int, Dict[str, Any]]:
    """
    Process a single QA dataset split. Runs retrieval for T, T+E, and T+E+K.
    Returns (num_questions_processed, stats_dictionary).
    """
    print(f"\n[INFO] Loading {split_name} from {input_path}")
    df_qa = pd.read_parquet(input_path)

    # ── FIX: join sparsity here, before per-row extraction ────────────────────
    print(f"[INFO] Joining sparsity features for {split_name}...")
    df_qa = join_sparsity(df_qa)
    # ── end fix ───────────────────────────────────────────────────────────────

    records: list[dict] = []

    expected_struct_cols = ["n_labs", "n_diag", "n_meds", "sparsity_score", "sparsity_bucket"]
    if all(col in df_qa.columns for col in expected_struct_cols):
        print("[INFO] Structural routing features detected.")
    else:
        present = [c for c in expected_struct_cols if c in df_qa.columns]
        missing = [c for c in expected_struct_cols if c not in df_qa.columns]
        print(f"[INFO] Structural features present: {present}")
        print(f"[WARN] Missing (will use 0/unknown): {missing}")

    stats = {
        Mode.T.value:   {"latency": 0.0, "tokens": 0, "kg_facts": 0, "count": 0},
        Mode.TE.value:  {"latency": 0.0, "tokens": 0, "kg_facts": 0, "count": 0},
        Mode.TEK.value: {"latency": 0.0, "tokens": 0, "kg_facts": 0, "count": 0},
    }

    total_rows = len(df_qa)
    print(f"[INFO] Processing {total_rows} questions...")

    for idx, row in df_qa.iterrows():
        question   = str(row.get("question", ""))
        answer     = str(row.get("answer", ""))
        hadm_id    = int(row["hadm_id"]) if pd.notna(row.get("hadm_id")) else None
        subject_id = (int(row["subject_id"])
                      if "subject_id" in row and pd.notna(row.get("subject_id"))
                      else None)

        # These now come from the sparsity join, not from the QA file
        n_labs_val = row.get("n_labs", 0)
        n_labs = int(n_labs_val) if pd.notna(n_labs_val) else 0

        n_diag_val = row.get("n_diag", 0)
        n_diag = int(n_diag_val) if pd.notna(n_diag_val) else 0

        # n_meds is not in sparsity.parquet; keep reading from QA file (may be 0)
        n_meds_val = row.get("n_meds", 0)
        n_meds = int(n_meds_val) if pd.notna(n_meds_val) else 0

        ss_val = row.get("sparsity_score", 0.0)
        sparsity_score = float(ss_val) if pd.notna(ss_val) else 0.0

        sb_val = row.get("sparsity_bucket", "unknown")
        sparsity_bucket = str(sb_val) if pd.notna(sb_val) else "unknown"

        results = retriever.retrieve_all_modes(question=question, hadm_id=hadm_id)

        for mode_name, result in results.items():
            r_stats = result.stats

            records.append({
                "question":              question,
                "answer":                answer,
                "hadm_id":               hadm_id,
                "subject_id":            subject_id,
                "mode":                  mode_name,
                "retrieved_chunks":      json.dumps(result.retrieved_chunks,
                                                    default=numpy_json_encoder),
                "has_ehr":               r_stats["has_ehr"],
                "n_kg_facts":            r_stats["n_kg_facts"],
                "prompt_context":        result.prompt_context,
                "prompt_token_estimate": r_stats["n_tokens_approx"],
                "retrieval_latency_ms":  r_stats["latency_ms"],
                "n_labs":                n_labs,
                "n_diag":                n_diag,
                "n_meds":                n_meds,
                "sparsity_score":        sparsity_score,
                "sparsity_bucket":       sparsity_bucket,
            })

            stats[mode_name]["latency"]  += r_stats["latency_ms"]
            stats[mode_name]["tokens"]   += r_stats["n_tokens_approx"]
            stats[mode_name]["kg_facts"] += r_stats["n_kg_facts"]
            stats[mode_name]["count"]    += 1

        processed_count = idx + 1 if isinstance(idx, int) else len(records) // 3
        if processed_count % 100 == 0 or processed_count == total_rows:
            print(f"  [{split_name}] Processed {processed_count}/{total_rows} questions...")

    df_out = pd.DataFrame(records)

    # Validate output has real sparsity values (not all unknown)
    unknown_frac = (df_out["sparsity_bucket"] == "unknown").mean()
    if unknown_frac > 0.5:
        print(f"[WARN] {unknown_frac:.1%} of rows still have sparsity_bucket='unknown'. "
              f"Check that hadm_ids in the QA file match sparsity.parquet.")
    else:
        print(f"[INFO] Sparsity validation passed: "
              f"{(1-unknown_frac):.1%} of rows have real sparsity values.")

    missing_cols = [c for c in expected_struct_cols if c not in df_out.columns]
    if missing_cols:
        raise ValueError(f"Output validation failed: missing columns {missing_cols}")

    output_path.parent.mkdir(parents=True, exist_ok=True)
    df_out.to_parquet(output_path, index=False)
    print(f"[INFO] Saved {len(df_out)} contextual rows to {output_path}")

    return total_rows, stats


def print_summary(split_name: str, n_questions: int, stats: Dict[str, Any]) -> None:
    print(f"\n{'═'*60}")
    print(f"SUMMARY: {split_name.upper()}")
    print(f"{'═'*60}")
    print(f"  Questions Processed  : {n_questions:,}")
    print(f"  Retrieval Contexts   : {n_questions * 3:,} (3 per question)")
    print("\n  Averages per Retrieval Mode:")

    for mode in [Mode.T.value, Mode.TE.value, Mode.TEK.value]:
        s = stats[mode]
        c = s["count"] if s["count"] > 0 else 1
        print(f"    [{mode}]")
        print(f"      Latency        : {s['latency'] / c:.1f} ms")
        print(f"      Prompt Length  : {s['tokens'] / c:.0f} tokens")
        print(f"      KG Facts Mined : {s['kg_facts'] / c:.1f}")


# ── Main ──────────────────────────────────────────────────────────────────────

def main():
    print("[INFO] Starting Router Dataset Generation Pipeline...")

    train_path = get_input_path(INPUT_TRAIN_A, INPUT_TRAIN_B)
    val_path   = get_input_path(INPUT_VAL_A,   INPUT_VAL_B)

    kg_module = load_kg_module()
    print("[INFO] Initializing Unified Retriever...")
    retriever = Retriever(kg_module=kg_module)

    try:
        train_q_count, train_stats = process_split(
            split_name="router_train",
            input_path=train_path,
            output_path=OUT_TRAIN,
            retriever=retriever,
        )
        print_summary("Router Train Split", train_q_count, train_stats)

        val_q_count, val_stats = process_split(
            split_name="router_val",
            input_path=val_path,
            output_path=OUT_VAL,
            retriever=retriever,
        )
        print_summary("Router Val Split", val_q_count, val_stats)

    finally:
        retriever.close()
        print("\n[INFO] Pipeline Completed Safely.")


if __name__ == "__main__":
    main()